    "def haversine_distance(lat1, lon1, lat2, lon2) -> float:\n",
    "    \"\"\"\n",
    "    lat, lon tính bằng độ. Kết quả trả về mét.\n",
    "    \"\"\"\n",
    "    R = 6371000.0  # bán kính Trái Đất (m)\n",
    "\n",
    "    phi1 = radians(lat1)\n",
    "    phi2 = radians(lat2)\n",
    "    dphi = radians(lat2 - lat1)\n",
    "    dlambda = radians(lon2 - lon1)\n",
    "\n",
    "    a = sin(dphi / 2.0) ** 2 + cos(phi1) * cos(phi2) * sin(dlambda / 2.0) ** 2\n",
    "    c = 2 * asin(sqrt(a))\n",
    "    return float(R * c)\n",
