    "    # 4) sort theo Wpos giảm dần\n",
    "    order = np.argsort(-W_sel)\n",
    "    W_sel = W_sel[order]\n",
    "    tau_sel = tau_sel[order]\n",
    "    corr_sel = corr_sel[order]\n",
    "    dist_sel = dist_sel[order]\n",
