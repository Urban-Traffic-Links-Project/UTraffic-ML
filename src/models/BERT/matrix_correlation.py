    "# ============================================================\n",
    "# 1) Pearson time-lag cross-correlation cho một cặp (i, j)\n",
    "# ============================================================\n",
    "def cross_corr_time_lag_pair(\n",
    "    x: np.ndarray,y:np.ndarray,tau_max:int\n",
    ") -> Tuple[np.ndarray,np.ndarray]:\n",
    "    assert x.ndim == 1 and y.ndim == 1\n",
    "    assert len(x) == len(y)\n",
    "    L = len(x)\n",
    "    taus = np.arange(-tau_max, tau_max + 1, dtype=int)\n",
    "    corrs = np.zeros_like(taus,dtype=np.float32)\n",
    "\n",
    "    def _pearson(a: np.ndarray, b: np.ndarray) -> float:\n",
    "        if a.size < 2:\n",
    "            return 0.0\n",
    "        a_mean = a.mean()\n",
    "        b_mean = b.mean()\n",
    "        a_centered = a - a_mean\n",
    "        b_centered = b - b_mean\n",
    "\n",
    "        num = np.sum(a_centered * b_centered)\n",
    "        den = np.sqrt(np.sum(a_centered**2) * np.sum(b_centered**2))\n",
    "        if den <= 1e-12:\n",
    "            return 0.0\n",
    "        return float(num / den)\n",
    "    for k, tau in enumerate(taus):\n",
    "        if tau == 0:\n",
    "            a = x\n",
    "            b = y\n",
    "        elif tau > 0:\n",
    "            a = x[: L - tau]\n",
    "            b = y[tau:]\n",
    "        else:\n",
    "            lag = -tau\n",
    "            a = y[: L - lag]\n",
    "            b = x[lag:]\n",
    "        corrs[k] = _pearson(a, b)\n",
    "    return taus, corrs\n",
    "\n",
    "# ============================================================\n",
    "# 2) Link weight W^{pos}_{i,j} như paper\n",
    "# ============================================================\n",
    "\n",
    "def link_weight_pos(x: np.ndarray,y: np.ndarray,tau_max: int) -> Tuple[float, int, float]:\n",
    "    taus, corrs = cross_corr_time_lag_pair(x, y, tau_max)\n",
    "    max_corr = float(np.max(corrs))\n",
    "    tau_peak = int(taus[np.argmax(corrs)])\n",
    "    mean_corr = float(np.mean(corrs))\n",
    "    std_corr = float(np.std(corrs))\n",
    "    if std_corr < 1e-6:\n",
    "        std_corr = 1e-6\n",
    "    Wpos = (max_corr - mean_corr) / std_corr\n",
    "    return float(Wpos), tau_peak, max_corr\n",
