    "def build_influenced_subgraph(\n",
    "    output_dir: Union[str, Path],\n",
    "    corr_info: Dict[str, Any],\n",
    "    tau_cut: int = 40,\n",
    "    Wmin: float = None,\n",
    "    Dmax: float = None,\n",
