    "def build_laplacian_from_distance(\n",
    "    subgraph_info: Dict[str, Any],\n",
    "    d_spa: int = 16,\n",
    "    sigma: float = 500.0,\n",
    ") -> Dict[str, Any]:\n",
    "    \"\"\"\n",
    "    Xây adjacency + Laplacian eigenvectors cho subgraph\n",
    "    dùng khoảng cách Haversine tới anchor (mét):\n",
    "\n",
    "      - node 0: anchor\n",
    "      - node 1..k: neighbors\n",
    "      - A[0,i] = A[i,0] = exp(-dist_i / sigma)\n",
    "\n",
    "    sigma: tham số scale (m). Ví dụ:\n",
    "      - 500 → edge weight ~0.37 tại 500m\n",
    "      - 1000 → edge weight ~0.37 tại 1km\n",
    "    \"\"\"\n",
    "    dist_to_anchor = subgraph_info[\"dist_to_anchor\"]  # (k,) mét\n",
    "    k = dist_to_anchor.shape[0]\n",
    "    n_nodes = k + 1\n",
    "\n",
    "    A = np.zeros((n_nodes, n_nodes), dtype=\"float32\")\n",
    "    for i in range(1, n_nodes):\n",
    "        d = float(dist_to_anchor[i - 1])  # mét\n",
    "        w = float(np.exp(-d / sigma))\n",
    "        A[0, i] = w\n",
    "        A[i, 0] = w\n",
    "\n",
    "    d_vec = A.sum(axis=1)\n",
    "    D = np.diag(d_vec)\n",
    "    L = D - A\n",
    "\n",
    "    evals, evecs = np.linalg.eigh(L)\n",
    "    idx = np.argsort(evals)\n",
    "    if d_spa < n_nodes:\n",
    "        idx = idx[:d_spa]\n",
    "    lap_eigvec = evecs[:, idx].astype(\"float32\")  # (n_nodes, d_spa)\n",
    "\n",
    "    out = dict(subgraph_info)\n",
    "    out.update({\n",
    "        \"adjacency\": A,\n",
    "        \"lap_eigvec\": lap_eigvec,\n",
    "    })\n",
    "    return out\n",
    "\n",
