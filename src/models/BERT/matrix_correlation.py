    "\n",
    "    mask2 = (W_candidates >= Wmin) & (dist_candidates <= Dmax)\n",
    "\n",
    "    W_sel = W_candidates[mask2]\n",
    "    tau_sel = tau_candidates[mask2]\n",
    "    corr_sel = corr_candidates[mask2]\n",
    "    dist_sel = dist_candidates[mask2]\n",
    "    idx_sel = idx_candidates[mask2]\n",
    "\n",
    "    # bỏ anchor nếu lỡ dính\n",
    "    idx_sel = idx_sel[idx_sel != anchor_idx]\n",
